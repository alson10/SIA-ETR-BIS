    public function toggle($id, $id_status)
    {
        $user = User::findOrFail($id);
        $msg = "";
        if ($user && $id_status == 1) {
            $user->id_status = 1;
            $msg = 'ID has been verified';
        } else if ($user && $id_status == 2) {
            $user->id_status = 2;
            $msg = 'ID is not verified';
        }
        $user->save();
        return redirect()->back()->with('message', $msg);
    }
